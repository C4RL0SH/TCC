    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTable1.getSelectedRow() != -1) {
        Produto produto = new Produto();
        ProdutoDAO dao = new ProdutoDAO();
        
        produto.setNome_produto(nome.getText());
        produto.setNome_fornecedor(fornecedor.getText());
        produto.setCodigo_produto(Integer.parseInt(codigo.getText()));
        produto.setData_registro(data.getDate());
        produto.setCategoria(categoria.getText());
        produto.setQuantidade(Integer.parseInt(quant.getText()));
        produto.setTamanho(tamanho.getText());
        produto.setPreco_produto(Double.parseDouble(precop.getText()));
        produto.setPreco_compra(Double.parseDouble(preco.getText()));
        produto.setDesconto(Integer.parseInt(desconto.getText()));
        dao.atualizar(produto);
        
        nome.setText("");
        fornecedor.setText("");
        codigo.setText("");
        data.setDateFormatString("");
        quant.setText("");
        tamanho.setText("");
        precop.setText("");
        preco.setText("");
        desconto.setText("");
        
        readJTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

