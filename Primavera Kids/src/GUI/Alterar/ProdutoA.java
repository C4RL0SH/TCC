    public void readJTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();
        
        for(Produto p: pdao.read()){
            
            modelo.addRow(new Object[]{
            
             p.getId_produto(),
             p.getCodigo_produto(),
             p.getNome_produto(),
             p.getNome_fornecedor(),
             p.getData_registro(),
             p.getCategoria(),
             p.getQuantidade(),
             p.getPreco_produto(),
             p.getPreco_compra(),
             p.getDesconto()
            
            });
        
        }}
    
